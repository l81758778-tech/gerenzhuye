window.initJournalLightField = function initJournalLightField(selector) {
  const canvas = document.querySelector(selector);
  if (!canvas) return;
  const body = document.body;
  const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");
  const gl = canvas.getContext("webgl", { alpha: true, antialias: false, depth: false, powerPreference: "high-performance" });
  if (!gl) {
    body.classList.add("no-webgl");
    return;
  }

  function shader(type, source) {
    const item = gl.createShader(type);
    gl.shaderSource(item, source);
    gl.compileShader(item);
    return gl.getShaderParameter(item, gl.COMPILE_STATUS) ? item : null;
  }

  const vertex = shader(gl.VERTEX_SHADER, "attribute vec2 p;void main(){gl_Position=vec4(p,0.,1.);}");
  const fragment = shader(gl.FRAGMENT_SHADER, `precision highp float;uniform vec2 r;uniform vec2 m;uniform float t;float h(vec2 p){p=fract(p*vec2(123.34,456.21));p+=dot(p,p+45.32);return fract(p.x*p.y);}void main(){vec2 f=gl_FragCoord.xy;vec2 p=(f-.5*r)/min(r.x,r.y);p+=m*.025;float a=atan(p.y,p.x);float d=length(p*vec2(.92,1.06));float c=.285+sin(a*3.+t*.16)*.045+sin(a*5.-t*.27)*.018;float b=1.-smoothstep(c-.08,c+.018,d);float rim=exp(-abs(d-c)*38.);vec2 l=vec2(-.32+sin(t*.11)*.08,.29+cos(t*.08)*.04);float key=pow(max(0.,1.-length(p-l)),3.4);float beam=exp(-abs(p.x*.46+p.y*.88+.12)*7.)*smoothstep(.86,.05,length(p-vec2(-.12,.08)));float grain=(h(f+floor(t*12.))-.5)*.018;float v=(b*(.025+key*.28)+rim*.2+beam*.06)*smoothstep(.88,.15,length(p))*.58+grain;gl_FragColor=vec4(vec3(v*.92,v*.94,v),1.);}`);
  if (!vertex || !fragment) {
    body.classList.add("no-webgl");
    return;
  }

  const program = gl.createProgram();
  gl.attachShader(program, vertex);
  gl.attachShader(program, fragment);
  gl.linkProgram(program);
  if (!gl.getProgramParameter(program, gl.LINK_STATUS)) {
    body.classList.add("no-webgl");
    return;
  }

  const buffer = gl.createBuffer();
  gl.bindBuffer(gl.ARRAY_BUFFER, buffer);
  gl.bufferData(gl.ARRAY_BUFFER, new Float32Array([-1, -1, 3, -1, -1, 3]), gl.STATIC_DRAW);
  const position = gl.getAttribLocation(program, "p");
  const resolution = gl.getUniformLocation(program, "r");
  const mouse = gl.getUniformLocation(program, "m");
  const time = gl.getUniformLocation(program, "t");
  let pointerX = 0;
  let pointerY = 0;
  let previousFrame = 0;

  function draw(timestamp) {
    window.requestAnimationFrame(draw);
    if (document.hidden || timestamp - previousFrame < 32) return;
    previousFrame = timestamp;
    const dpr = Math.min(window.devicePixelRatio || 1, 1.5);
    const width = Math.max(1, Math.floor(window.innerWidth * dpr));
    const height = Math.max(1, Math.floor(window.innerHeight * dpr));
    if (canvas.width !== width || canvas.height !== height) {
      canvas.width = width;
      canvas.height = height;
      gl.viewport(0, 0, width, height);
    }
    gl.useProgram(program);
    gl.enableVertexAttribArray(position);
    gl.bindBuffer(gl.ARRAY_BUFFER, buffer);
    gl.vertexAttribPointer(position, 2, gl.FLOAT, false, 0, 0);
    gl.uniform2f(resolution, width, height);
    gl.uniform2f(mouse, pointerX, pointerY);
    gl.uniform1f(time, timestamp * 0.001);
    gl.drawArrays(gl.TRIANGLES, 0, 3);
  }

  if (!reducedMotion.matches && window.matchMedia("(pointer: fine)").matches) {
    window.addEventListener("pointermove", (event) => {
      pointerX = (event.clientX / window.innerWidth - 0.5) * 2;
      pointerY = (event.clientY / window.innerHeight - 0.5) * 2;
    }, { passive: true });
  }
  window.requestAnimationFrame(draw);
};
